package org.test;

public class ArithmeticOperation {

	public void arithmetic(int a, int b) {
		System.out.println(a + b);
	}

	public void arithmetic(int a, float b) {
		System.out.println(a - b);
	}

	public void arithmetic(int a, double b) {
		System.out.println(a * b);

	}

	public void arithmetic(float a, int b) {
		System.out.println(a / b);
	}

}
